export const getIcon = (key) => {
    if(key === 'revenue'){
        return 'TrendingUpIcon'
    } else if(key === 'tip') {
        return 'AwardIcon'
    } else if(key === 'paid_orders') {
        return 'SmileIcon'
    } else if(key === 'unpaid_orders') {
        return 'MehIcon'
    } else if(key === 'fulfilled_orders') {
        return 'PackageIcon'
    } else if(key === 'cancelled_orders') {
        return 'ZapOffIcon'
    } else if(key === 'cancelled_orders') {
        return 'ThumbsDownIcon'
    } else if(key === 'cancelled_orders') {
        return 'ThumbsDownIcon'
    } else if(key === 'cancelled_orders') {
        return 'ThumbsDownIcon'
    }

    
    
    return 'TrendingUpIcon'
}